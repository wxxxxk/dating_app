const DEG = Math.PI / 180;

function seriesSum(terms, tau) {
  let sum = 0;
  for (let i = 0; i < terms.length; i += 1) {
    const t = terms[i];
    sum += t[0] * Math.cos(t[1] + t[2] * tau);
  }
  return sum;
}

function polySum(groups, tau) {
  let total = 0;
  for (let i = groups.length - 1; i >= 0; i -= 1) {
    total = total * tau + seriesSum(groups[i], tau);
  }
  return total;
}

function normDeg(x) {
  const v = x % 360;
  return v < 0 ? v + 360 : v;
}

/**
 * ΔT = TT − UT (초). Espenak & Meeus 다항식.
 *
 * 이 값을 빼먹으면 절입 시각이 현대 기준 약 1분 어긋난다.
 * 앱이 받는 생년(1940~현재) 범위를 덮는 구간만 둔다.
 */
function deltaTSeconds(year) {
  let t;
  if (year < 1920) {
    t = year - 1900;
    return (
      -2.79 + 1.494119 * t - 0.0598939 * t * t + 0.0061966 * t ** 3 - 0.000197 * t ** 4
    );
  }
  if (year < 1941) {
    t = year - 1920;
    return 21.2 + 0.84493 * t - 0.0761 * t * t + 0.0020936 * t ** 3;
  }
  if (year < 1961) {
    t = year - 1950;
    return 29.07 + 0.407 * t - (t * t) / 233 + t ** 3 / 2547;
  }
  if (year < 1986) {
    t = year - 1975;
    return 45.45 + 1.067 * t - (t * t) / 260 - t ** 3 / 718;
  }
  if (year < 2005) {
    t = year - 2000;
    return (
      63.86 +
      0.3345 * t -
      0.060374 * t * t +
      0.0017275 * t ** 3 +
      0.000651814 * t ** 4 +
      0.00002373599 * t ** 5
    );
  }
  if (year < 2050) {
    t = year - 2000;
    return 62.92 + 0.32217 * t + 0.005589 * t * t;
  }
  t = (year - 1820) / 100;
  return -20 + 32 * t * t - 0.5628 * (2150 - year);
}

/** UTC Date → 율리우스일(UT 기준, 소수 포함). */
function julianDayFromUtc(dateUtc) {
  return dateUtc.getTime() / 86400000 + 2440587.5;
}

/**
 * 주어진 UTC 시점의 태양 겉보기 황경(도).
 *
 * VSOP87D 절단 + FK5 보정 + 장동 + 광행차 + ΔT.
 */
function sunApparentLongitude(dateUtc) {
  const jdUt = julianDayFromUtc(dateUtc);
  // 년의 소수 부분은 ΔT 다항식에 넣기엔 과하다 — 연 단위면 충분하다.
  const approxYear = dateUtc.getUTCFullYear();
  const jde = jdUt + deltaTSeconds(approxYear) / 86400;

  const tau = (jde - 2451545.0) / 365250.0;
  const lRad = polySum([L0, L1, L2, L3, L4, L5], tau) / 1e8;
  const rAu = polySum([R0, R1, R2, R3, R4], tau) / 1e8;

  // 지구 헬리오센트릭 황경 → 태양 지오센트릭 황경.
  const theta = normDeg((lRad / DEG) + 180);

  // VSOP87 → FK5 보정.
  const t = tau * 10;
  const lambdaPrime = theta - 1.397 * t - 0.00031 * t * t;
  const thetaFk5 =
    theta -
    0.09033 / 3600 +
    // β(지구 위도)는 1″ 미만이라 황경 보정에 미치는 영향이 무시 가능하다.
    (0.03916 / 3600) *
      (Math.cos(lambdaPrime * DEG) - Math.sin(lambdaPrime * DEG)) *
      0;

  // 장동(황경). Meeus 저정밀 항으로 0.5″ 이내.
  const omega = 125.04452 - 1934.136261 * t;
  const lSun = 280.4665 + 36000.7698 * t;
  const lMoon = 218.3165 + 481267.8813 * t;
  const deltaPsiArcsec =
    -17.2 * Math.sin(omega * DEG) -
    1.32 * Math.sin(2 * lSun * DEG) -
    0.23 * Math.sin(2 * lMoon * DEG) +
    0.21 * Math.sin(2 * omega * DEG);

  // 광행차.
  const aberrationArcsec = -20.4898 / rAu;

  return normDeg(thetaFk5 + (deltaPsiArcsec + aberrationArcsec) / 3600);
}

